package model;

/**
 * <b>PlayListType</b> is a enumeration that represents a playlist type of the application.
 * @author Yeison Antonio Rodriguez Zuluaga.
 */
public enum PlayListType {

    /**
     * <b>SONG</b><br> represent a song playlist type.
     */
    SONG,
    /**
     * <b>PODCAST</b><br> represent a podcast playlist type.
     */
    PODCAST,
    /**
     * <b>MIX</b><br> represent a mix playlist type.
     */
    MIX;
    
}
